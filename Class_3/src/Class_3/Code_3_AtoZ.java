package Class_3;

public class Code_3_AtoZ {

	public static void main(String[] args) {
		// Print in console (A to Z) & (a to z)
		

		char ch;
		
		for(ch = 'A'; ch <='Z'; ch++) {
		
			System.out.println("A to Z = "+ch); 
		}
		
		for(ch = 'a'; ch <='z'; ch++) {
			
			System.out.println("a to z = "+ch); 
		}
	}

}
