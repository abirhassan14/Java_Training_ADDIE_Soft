package Class_5;

public class Array1 {

	public static void main(String[] args) {
		// Java Array
		
		int [] m = new int [5];
		m[0] = 10;
		m[1] = 20;
		m[2] = 30;
		m[3] = 40;
		m[4] = 50;
		
		for (int i=0; i<=4; i++) {		// Always use loop to access array data
			System.out.print(m[i]+ ", ");
		}

	}

}
