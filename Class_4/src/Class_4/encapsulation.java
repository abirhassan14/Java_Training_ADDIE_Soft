package Class_4;

public class encapsulation {

	private String name;
	private String email;
	private int age;
	
	public void setname(String name) {
	this.name = name;
	
	}

	public void setemail(String email) {
	this.email = email;
	
	}
	
	public void setage(int age) {
	this.age = age;
		
	}

	public String getname() {
		return name;
	}
	public String getemail() {
		return email;
	}
	public int getage() {
		return age;
	}
}

